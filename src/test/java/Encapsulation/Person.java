package Encapsulation;

public class Person {
	private String  name;
	private int age;
	
	

	public String getName() {
		return name;
	}

	public void setName(String myname) {
		this.name = myname;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int myage) {
		this.age = myage;
	}
	
}
